'''
.strip():                           Loại bỏ khoảng trắng ở đầu và cuối chuỗi.
.split():                           Chia chuỗi thành danh sách các phần tử dựa trên ký tự phân tách.
.join():                            Kết hợp danh sách các chuỗi thành một chuỗi với một ký tự phân tách.
.replace():                         Thay thế tất cả các xuất hiện của một chuỗi con bằng một chuỗi khác.
.find():                            Tìm vị trí đầu tiên của một chuỗi con trong chuỗi. Nếu không tìm thấy, trả về -1.
.count():                           Đếm số lần xuất hiện của một chuỗi con trong chuỗi.
.upper() và .lower():               Chuyển đổi chuỗi thành chữ hoa hoặc chữ thường.
.startswith() và .endswith():       Kiểm tra xem chuỗi có bắt đầu hoặc kết thúc bằng một chuỗi con cụ thể không.
.capitalize():                      Chuyển đổi ký tự đầu tiên thành chữ hoa và tất cả các ký tự khác thành chữ thường.
.title():                           Chuyển đổi ký tự đầu tiên của mỗi từ thành chữ hoa.
'''